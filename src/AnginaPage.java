    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed

        try{
            String sql="select * from Angina where Name =?";
            pst=con.prepareStatement(sql);
            pst.setString(1,jTextField_Search.getText());
            rs=pst.executeQuery();
            if(rs.next())
            {
                String s1=rs.getString("Name");
                jTextFieldN.setText(s1);
                String s2=rs.getString("Power");
                jTextFieldP.setText(s2);
                String s3=rs.getString("Price");
                jTextFieldPb.setText(s3);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButtonSearchActionPerformed

