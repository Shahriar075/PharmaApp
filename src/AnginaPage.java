    private void Table_AnginaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_AnginaMouseClicked
       try{
           int row=Table_Angina.getSelectedRow();
           String click=(Table_Angina.getModel().getValueAt(row,0).toString());
           String sql="select * from Angina where Name='"+click+"' ";
          pst=con.prepareStatement(sql);
          rs=pst.executeQuery();
          if(rs.next()){
                String s1=rs.getString("Name");
                jTextFieldN.setText(s1);
                String s2=rs.getString("Power");
                jTextFieldP.setText(s2);
                String s3=rs.getString("Price");
                jTextFieldPb.setText(s3);
          }
       }
       catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
    }//GEN-LAST:event_Table_AnginaMouseClicked

